package sual_34;

/**
 * İki ədədin cəmini və fərqini geri qaytaran iki parametrli metod yazın.
 */
public class Main {
    public static double cem(double a, double b) {
        return a + b;
    }

    public static double ferq(double a, double b) {
        return a - b;
    }

    public static void main(String[] args) {
        System.out.println(cem(2, 3));
        System.out.println(ferq(5, 9));

    }
}
